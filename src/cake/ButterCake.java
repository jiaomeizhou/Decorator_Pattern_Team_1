package cake;

/**
 * This class represents the ButterCake. It extends all the operation mandated by the Cake class.
 */
public class ButterCake extends Cake {

  /**
   * Constructs a ButterCake class and initializes it to the given description and price.
   */
  public ButterCake() {
    this.description = "Butter Cake";
    this.price = 4.99;
  }
}
